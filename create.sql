CREATE TABLE categorias (
    cat_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    cat_num NUMBER NOT NULL,
    cat_color VARCHAR2(255 CHAR) NOT NULL,
    cat_name VARCHAR2(25 CHAR) NOT NULL,
    PRIMARY KEY (cat_id)
);
CREATE TABLE documentos (
    doc_id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    doc_num NUMBER NOT NULL,
    doc_digit VARCHAR2(11 CHAR) NOT NULL,
    doc_name VARCHAR2(225 CHAR) NOT NULL,
    doc_body CLOB NOT NULL,
    PRIMARY KEY (doc_id)
);
CREATE TABLE usuarios (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY,
    nome VARCHAR2(255 CHAR),
    email VARCHAR2(255 CHAR) NOT NULL,
    senha VARCHAR2(255 CHAR) NOT NULL,
    PRIMARY KEY (id)
);
